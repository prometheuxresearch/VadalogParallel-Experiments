package uk.co.prometheux.prometheuxreasoner.rest.responses;

import org.springframework.http.HttpStatus;
import org.springframework.http.ResponseEntity;

import uk.co.prometheux.prometheuxreasoner.PrometheuxRuntimeException;
import uk.co.prometheux.prometheuxreasoner.errorMessage.ErrorMessage;
import uk.co.prometheux.prometheuxreasoner.parser.ParsingException;
import uk.co.prometheux.prometheuxreasoner.planner.PlanningException;

/**
 * A factory to handle the correct response to the evaluation of a program.
 * 
 * 
 * Copyright (C) Prometheux Limited. All rights reserved.
 * 
 * @author Prometheux Limited
 */

public class ErrorResponseFactory {

	public ResponseEntity<PrometheuxResponseBody> getResponse(Exception e) {
		ResponseEntity<PrometheuxResponseBody> response = null;
		/*
		 * If it is a Prometheux Runtime Exception we know how to handle it OR if the
		 * error message is not null we know this exception, so we get the custom error
		 * message, otherwise we produce a generic error code and we keep the error
		 * message generated by the exception
		 */
		if (e instanceof PrometheuxRuntimeException && ((PrometheuxRuntimeException) e).getErrorMessage() != null) {
			ErrorMessage errorMessage = ((PrometheuxRuntimeException) e).getErrorMessage();
			String message = errorMessage.getMessage();
			String errorCode = errorMessage.getErrorCode();
			ErrorResponseBody responseBody = new ErrorResponseBody(message, errorCode);
			if (e instanceof PlanningException || e instanceof ParsingException) {
				response = new ResponseEntity<PrometheuxResponseBody>(responseBody, HttpStatus.BAD_REQUEST);
			} else {
				response = new ResponseEntity<PrometheuxResponseBody>(responseBody, HttpStatus.INTERNAL_SERVER_ERROR);
			}
		}
		/* It is an exception that we have not still handled */
		else {
			PrometheuxRuntimeException pmtxException = new PrometheuxRuntimeException(e.getMessage());
			ErrorMessage errorMessage = pmtxException.getErrorMessage();
			String truncatedErrorMessage = errorMessage.getMessage().substring(0, Math.min(errorMessage.getMessage().length(), 400))+"[...]";
			ErrorResponseBody responseBody = new ErrorResponseBody(truncatedErrorMessage, errorMessage.getErrorCode());
			// for now internal server error
			response = new ResponseEntity<PrometheuxResponseBody>(responseBody, HttpStatus.INTERNAL_SERVER_ERROR);
		}
		return response;
	}

}